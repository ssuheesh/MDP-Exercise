@Composable
fun Greeting(name: String, modifier: Modifier = Modifier) {
    Text(
        text = "Hello $name!",
        modifier = modifier.fillMaxWidth()
            .background(color=Color.LightGray)
            .fillMaxHeight(),
        textAlign = TextAlign.Center,
        fontWeight = FontWeight.Bold,
        color = Color.Red,
        fontSize = 25.sp
    )
}

